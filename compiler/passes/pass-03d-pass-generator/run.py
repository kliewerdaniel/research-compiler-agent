#!/usr/bin/env python3
"""pass-03d-pass-generator entrypoint (deterministic).

The compiler grows itself. It reads the self-improvement backlog in
``agents/self_improvement.md`` (a "ledger" special dependency — editing the
ledger invalidates this pass), selects the top unresolved ``- [ ]`` proposal
under the ``## Backlog`` heading, and scaffolds a new pass directory under
``compiler/passes/``:

    pass-NN-<slug>/
        pass.yaml     declarative metadata (id, consumes, produces, ...)
        run.py        deterministic stub that, when run, writes a <id>-ir
                      artifact capturing the proposed capability (so the
                      generated pass is inspectable + runnable immediately)
        prompt.md     the natural-language spec for the capability, consumed
                      by the generated pass when it later runs with --local

The next compiler run discovers the generated pass and executes it, closing
the loop: the backlog proposes a capability, the generator materialises it,
and the generated pass contributes to the knowledge graph / research output.

This makes the self-improvement ledger a *living* work queue: resolving an item
is done by the compiler, not by a human editing code.

Invocation: python run.py <build_dir>
"""

